function CarregarTabela(dados) {
    let tabela = document.querySelector("#tableBody");

    tabela.innerHTML = "";

    let linhas = "";

    dados.forEach((dado) => {

        linhas +=
            `<tr>
                    <td>${dado.codigo}</td >
                    <td>${dado.nome}</td>
                    <td>${dado.valor}</td>
                </tr>`
    }
    )
    tabela.innerHTML = linhas;
}

function alterarValorTotal() {
    var total = 0;
    $('[name=quantidadeProdutos]').each(function () {
        if ($(this).val() < 0) {
            $(this).val(0)
        }
        if ($(this).val() != '') {
            var quantidade = parseFloat($(this).val());
            var valor = parseFloat($(this).parent().parent().find('[name=tdValor]').find('input').val().replace(',','.'));

            total += parseFloat(quantidade * valor);
        }

    })
    $('#lbTotal').text(total.toFixed(2));
}

